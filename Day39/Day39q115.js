function Daysofweek(choice) {
    switch (choice) {
        case 1:
            console.log("It's Monday, First day of week");
            break;
        case 2:
            console.log("It's Tuesday, Second day of week");
            break;
        case 3:
            console.log("It's wednesday, Third day of week");
            break;
        case 4:
            console.log("It's Thursday, Fourth day of week");
            break;
        case 5:
            console.log("It's Friday, Fifth day of week");
            break;
        case 6:
            console.log("It's Saturday, Sixth day of week");
            break;
        case 7:
            console.log("It's Sunday, Seventh day of week");
            break;
        default:
            console.log("Invalid number of days");
    }
}
Daysofweek(1);
export {};
